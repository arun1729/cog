def get_word_embedding(word):
    """ splits word embedding string into a list of floats """
    return [float(x) for x in word_embeddings[word].split()]


class TorqueTest(unittest.TestCase):
    maxDiff = None

    @classmethod
    def setUpClass(cls):
        if not os.path.exists("/tmp/" + DIR_NAME):
            os.mkdir("/tmp/" + DIR_NAME)

    def test_torque_word_embedding_api(self):
        g = Graph(graph_name="test2", cog_home=DIR_NAME)
        g.put_embedding("orange", get_word_embedding("orange"))
        g.put_embedding("tangerine", get_word_embedding("tangerine"))
        g.put_embedding("apple", get_word_embedding("apple"))

        self.assertEqual(get_word_embedding("orange"), g.get_embedding("orange"))
        self.assertEqual(get_word_embedding("tangerine"), g.get_embedding("tangerine"))
        self.assertEqual(get_word_embedding("apple"), g.get_embedding("apple"))

        self.assertEqual(None, g.get_embedding("banana"))

        g.delete_embedding("orange")
        self.assertEqual(None, g.get_embedding("orange"))

        g.delete_embedding("tangerine")
        self.assertEqual(None, g.get_embedding("tangerine"))
        g.close()

    @classmethod
    def tearDownClass(cls):
        shutil.rmtree("/tmp/" + DIR_NAME)
        print("*** deleted test data.")


if __name__ == '__main__':
    unittest.main()
